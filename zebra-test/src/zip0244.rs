//! Contains test vectors for ZIP-0244 data (transaction IDs, signature hashes).
use lazy_static::lazy_static;

/// A ZIP-244 test vector with the transaction input and expected outputs.
pub struct TestVector {
    /// The serialized transaction.
    pub tx: Vec<u8>,
    /// The expected transaction ID.
    pub txid: [u8; 32],
    /// The expected auth digest.
    pub auth_digest: [u8; 32],
    /// The amounts of the outputs matching each transparent input
    pub amounts: Vec<i64>,
    /// The script pubkey of the outputs matching each transparent input
    pub script_pubkeys: Vec<Vec<u8>>,
    /// Which transparent input the ID refers to, if any.
    pub transparent_input: Option<u32>,
    /// The signature hash for shielded transactions.
    pub sighash_shielded: [u8; 32],
    /// The signature hash for the SIGHASH_ALL type.
    pub sighash_all: Option<[u8; 32]>,
    /// The signature hash for the SIGHASH_NONE type.
    pub sighash_none: Option<[u8; 32]>,
    /// The signature hash for the SIGHASH_SINGLE type.
    pub sighash_single: Option<[u8; 32]>,
    /// The signature hash for the SIGHASH_ALL type with SIGHASH_ANYONECANPAY set.
    pub sighash_all_anyone: Option<[u8; 32]>,
    /// The signature hash for the SIGHASH_NONE type with SIGHASH_ANYONECANPAY set.
    pub sighash_none_anyone: Option<[u8; 32]>,
    /// The signature hash for the SIGHASH_SINGLE type with SIGHASH_ANYONECANPAY set.
    pub sighash_single_anyone: Option<[u8; 32]>,
}

