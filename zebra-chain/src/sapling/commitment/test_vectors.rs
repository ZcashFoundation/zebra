fn point_from_hex<T: AsRef<[u8]>>(point_in_hex: T) -> jubjub::AffinePoint {
    let mut bytes = [0u8; 32];
    let _ = hex::decode_to_slice(point_in_hex, &mut bytes);

    jubjub::AffinePoint::from_bytes(bytes).unwrap()
}

